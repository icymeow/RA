# ================== 3) 工具函数：把“内容标签_list”解析成真正 list ==================
def parse_list_cell(x):
    """
    x 可能是：
    - 真 list: ['甜文','情有独钟']
    - 字符串形式: "['甜文', '情有独钟']"
    - 空/NaN
    """
    if x is None or (isinstance(x, float) and pd.isna(x)):
        return []
    if isinstance(x, list):
        return [str(i).strip() for i in x if str(i).strip()]
    if isinstance(x, str):
        s = x.strip()
        if not s:
            return []
        # 尝试把 "['a','b']" 转成 list
        try:
            v = ast.literal_eval(s)
            if isinstance(v, list):
                return [str(i).strip() for i in v if str(i).strip()]
        except Exception:
            pass
        # 退化：按空格拆
        return [i.strip() for i in s.split() if i.strip()]
    # 其他类型
    return [str(x).strip()] if str(x).strip() else []

def clean_tags(tags):
    """按白名单过滤 + 去重(保序)"""
    seen = set()
    out = []
    for t in tags:
        if t in VALID_TAGS and t not in seen:
            seen.add(t)
            out.append(t)
    return out

# ================== 4) 主流程：读取 -> 清洗 -> 输出 ==================
def main():
    df = pd.read_excel(INPUT_XLSX)

    # 统一从 内容标签_list 优先清洗；如果没有这一列，就从 内容标签 拆
    if "内容标签_list" in df.columns:
        df["内容标签_list"] = df["内容标签_list"].apply(parse_list_cell)
    else:
        df["内容标签_list"] = df.get("内容标签", "").fillna("").apply(lambda x: str(x).split())

    df["内容标签_list"] = df["内容标签_list"].apply(clean_tags)
    df["内容标签"] = df["内容标签_list"].apply(lambda x: " ".join(x))

    # 可选：清洗后如果内容标签为空，设为 NaN（方便统计缺失）
    df.loc[df["内容标签"].eq(""), "内容标签"] = pd.NA

    df.to_excel(OUTPUT_XLSX, index=False)
    print(f"✅ 清洗完成，已输出：{OUTPUT_XLSX}")
    print("示例（前5行）：")
    cols = [c for c in ["作品", "发表时间", "内容标签", "内容标签_list"] if c in df.columns]
    print(df[cols].head(5).to_string(index=False))

if __name__ == "__main__":
    main()
